def drawScarf(): #Draws the scarf
    turtle.pencolor("dark red")
    turtle.setheading(320)
    turtle.penup()
    turtle.goto(-33,50)
    turtle.pendown()
    turtle.fillcolor('red')
    turtle.begin_fill()
    turtle.circle(50, extent = 80)    
    turtle.setheading(270)
    turtle.forward(30)
    turtle.setheading(45)
