    turtle.goto(-16,55)
    turtle.pendown()
    turtle.setheading(0)
    turtle.forward(32)
    turtle.penup()
    turtle.goto(-10,66)
    turtle.pendown()
    turtle.fillcolor('white')
    turtle.begin_fill()
    turtle.circle(5)
    turtle.end_fill()
    turtle.penup()
    turtle.goto(10,66)
    turtle.pendown()
    turtle.begin_fill()
    turtle.circle(5)
    turtle.end_fill()

