#This program will draw a snow man using turtle

#Importing turtle and configuring it 
import turtle
turtle.speed(0)
turtle.width(3)
turtle.bgcolor("light gray")

def drawBase(): #Draws the base
    turtle.penup()
    turtle.goto(0,-300)
    turtle.pendown()
    turtle.circle(100)

def drawMidSection(): #Draws the mid section
    turtle.penup()
    turtle.goto(0,-100)
    turtle.pendown()
    turtle.circle(66)
    
def drawArms(): #Draws the arms
    turtle.penup()
    turtle.goto(-66,-33)
    turtle.pendown()
    turtle.setheading(170)
    turtle.forward(65)
    turtle.right(60)
    turtle.forward(50)
    turtle.right(20)
    turtle.forward(10)
    turtle.backward(10)
    turtle.left(70)
    turtle.forward(10)    
    turtle.penup()
    turtle.goto(66,-33)
    turtle.pendown()
    turtle.setheading(30)
    turtle.forward(65)
    turtle.right(20)
    turtle.forward(10)
    turtle.backward(10)
    turtle.left(70)
    turtle.forward(10)    

    
def drawHead(): #Draws the head
    turtle.penup()
